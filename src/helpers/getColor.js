export const getTechnologyColor = (technology) => {
  switch (technology) {
    case 'React':
      return 'linkedin'
    case 'HTML':
      return 'orange'
    case 'CSS':
      return 'blue'
    case 'Javascript':
      return 'yellow'
    case 'Tailwind':
      return 'cyan'
    case 'Chakra UI':
      return 'cyan'
    case 'MongoDB':
      return 'green'
    case 'Bootsrap':
      return 'purple'
    case 'Prisma':
      return '#2D3748'
    case 'NextJS':
      return 'black'
  }
}
